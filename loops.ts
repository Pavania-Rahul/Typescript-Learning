//While loop
var i:number =1;
while(i<=10){
     console.log(i); //12345678910
     i++;
}


var i:number =2;
while(i<=10){
    console.log(i); //increment with 2  so output will be -->> 246810
    i+=2;
}

var i:number =10;

while(i>0){
  console.log(i); // ---->>10987654321
  i--;
}



//do while loop
var i:number =1;
do{
    console.log(i); //12345678910
    i++;

}while(i<=10);



//For Loop

for(var i:number=11;i<=20;i++){
    console.log(i); //--->> 11121314151617181920
}

